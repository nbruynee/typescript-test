// :type after defining a variable. Once the type is declared, we cannot change it 
// (static type)
// For example:
// + let variableName: type; => declares a variable, defines the type, but does not 
// initialize the value.
// + let variableName: type = value; => declares a variable, defines the type, and 
// assigns an initial value.
// const constantName: type = value;
const name1 = 'Bruyne';

console.log("length= ", name1.length);
console.log("upperCase= ", name1.toUpperCase());
